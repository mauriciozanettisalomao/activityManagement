
function showSuccess(message) {
	$("div.alert-success").html(message).fadeIn(300).delay(1500).fadeOut(1000);
};

function showError(message) {
	$("div.alert-danger").html(message).fadeIn(300).delay(1500).fadeOut(1000);
};

function showWarning(message) {
	$("div.alert-warning").html(message).fadeIn(300).delay(1500).fadeOut(1000);
};


	